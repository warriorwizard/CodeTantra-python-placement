#Arithmetic Operators are +, -, *, /, **, %, //
num1 = int(input("num1: "))
num2 = int(input("num2: "))

# print num1+num2
print(f'{num1} + {num2} = {num1+num2}')
# print num1-num2
print(f'{num1} - {num2} = {num1-num2}')
# print num1*num2
print(f'{num1} * {num2} = {num1*num2}')
# print num1/num2
print(f'{num1} / {num2} = {num1/num2}')
# print num1**num2
print(f'{num1} ** {num2} = {num1**num2}')
# print num1%num2
print(f"{num1} % {num2} = {num1%num2}")
# print num1//num2
print(f"{num1} // {num2} = {num1//num2}")